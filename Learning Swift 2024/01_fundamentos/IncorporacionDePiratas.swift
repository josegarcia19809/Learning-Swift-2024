 La incorporación de piratas

 Al capitán le gusta mucho escribir en una botella todo lo que hace para ﬁnalmente lanzarlo al mar, así en un futuro podrá ser recordado. A menudo, los piratas tienen batallas con otros barcos y en ocasiones algunos de ellos mueren en el combate. Al capitán le gusta la estadística y ha podido notar que el barco funciona a la perfección con exactamente 30 piratas. Esto quiere decir que cada vez que hay un combate tienen que reclutar tantos piratas como personas hayan muerto.

 Como se ha dicho, al capitán le gusta poner todo lo que hace en un mensaje para lanzarlo al mar, pero también es un poco vago. Como sabe de informática, hace un programa en swift en el que guarda en un array los nombres de los nuevos piratas y de los piratas fallecitos para escribir los siguientes mensajes:

 ✤ “Los (p: número de piratas fallecidos) piratas que han fallecido en combate son (nombre pirata 1), … (nombre pirata p) ” ✤ “Los (np: número de nuevos piratas) miembros de ésta tripulación son: (nombre pirata 1), … (nombre pirata np).”

 Como ambos mensajes se incluyen en el mismo papel de la misma botella al ﬁnal del mismo el capitán decide incluir la fecha, para que quede recordado en qué día lo hizo.

 Suponiendo que los piratas fallecidos son Juanjo, Marco, Pedro y Toni y los nuevos piratas son Alejandro, Cristóbal, Mario y Javier. El programa tiene que generar el mensaje:

 Los 4 piratas que han fallecido en combate son: ["Juanjo", "Marco", "Pedro", "Toni"] Los 4 nuevos miembros de ésta tripulación son: ["Alejandro", "Cristóbal", "Mario", "Javier"] Tuesday, July 4, 2017
